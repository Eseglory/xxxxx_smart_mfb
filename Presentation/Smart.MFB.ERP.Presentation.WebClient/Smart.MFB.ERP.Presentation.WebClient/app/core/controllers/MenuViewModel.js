
var menuMenu = angular.module('menu', ['schoolERPCommon'])
    .config(function ($routeProvider, $locationProvider) {
        $routeProvider.when(schoolERP.rootPath + 'core/menu', { templateUrl: schoolERP.rootPath + 'app/core/views/menu.html', controller: 'MenuListViewModel' });
        
        $routeProvider.otherwise({ redirectTo: schoolERP.rootPath + 'core/menu' });

		$locationProvider.html5Mode({
		  enabled: true,
		  requireBase: false
		});
    });

menuMenu.controller("MenuListViewModel", function ($scope, $http,$window, $location, viewModelHelper) {

    viewModelHelper.modelIsValid = true;
    viewModelHelper.modelErrors = [];

    $scope.viewMode = 'menulist'; // menulist, success
    $scope.menus = [];
   
    $scope.init = false; // used so view doesn't sit on "no available menus" while load takes place

    $scope.availableMenus = function () {
        viewModelHelper.apiGet('api/menu/availablemenuroles', null,
                function (result) {
                    $scope.menus = result.data;
					if ($scope.init === false)
						initializeView();
                    $scope.init = true;
                });
    }

	 $scope.previous = function () {
        $window.history.back();
    }

	var initializeView = function(){

	setTimeout(function () {
                
				var responsiveHelper_datatable_tabletools = undefined;

	var breakpointDefinition = {
            tablet: 1024,
            phone: 480
        };

	/* TABLETOOLS */
        var otable = $('#menuTable').DataTable({

            // Tabletools options:
            //   https://datatables.net/extensions/tabletools/button_options
            "sDom": "<'dt-toolbar'<'col-xs-12 col-sm-6'l><'col-sm-6 col-xs-6 hidden-xs'T>r>" +
                    "t" +
                    "<'dt-toolbar-footer'<'col-sm-6 col-xs-12 hidden-xs'i><'col-sm-6 col-xs-12'p>>",
            "oTableTools": {
                "aButtons": [
                "copy",
                "csv",
                "xls",
                   {
                       "sExtends": "pdf",
                       "sTitle": "All Menus",
                       "sPdfMessage": "Eglobalicthub ERP PDF Export",
                       "sPdfSize": "letter"
                   },
                   {
                       "sExtends": "print",
                       "sMessage": "Generated by Eglobalicthub <i>(press Esc to close)</i>"
                   }
                ],
                "sSwfPath": "/Scripts/plugin/datatables/swf/copy_csv_xls_pdf.swf"
            },
            "autoWidth": true,
            "preDrawCallback": function () {
                // Initialize the responsive datatables helper once.
                if (!responsiveHelper_datatable_tabletools) {
                    responsiveHelper_datatable_tabletools = new ResponsiveDatatablesHelper($('#menuTable'), breakpointDefinition);
                }
            },
            "rowCallback": function (nRow) {
                responsiveHelper_datatable_tabletools.createExpandIcon(nRow);
            },
            "drawCallback": function (oSettings) {
                responsiveHelper_datatable_tabletools.respond();
            }
        });

		// custom toolbar
        //$("div.toolbar").html('<div class="text-right"><img src="/Content/img/logo.png" alt="SmartAdmin" style="width: 111px; margin-top: 3px; margin-right: 10px;"></div>');

        // Apply the filter
        $("#menuTable thead th input[type=text]").on('keyup change', function () {

            otable
                .column($(this).parent().index() + ':visible')
                .search(this.value)
                .draw();

        });

        /* END TABLETOOLS */

            }, 50);
	}
  
    $scope.availableMenus();
});
